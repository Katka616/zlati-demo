/**
 * Material catalog — hierarchical: name → brands[] + types[]
 * Used for autocomplete suggestions in EstimateFormModal.
 *
 * Category codes match SPECIALIZATIONS from constants.ts (e.g. '01' = Plumber).
 * Technician can always override / type custom values — these are suggestions only.
 *
 * Structure:
 *   MaterialItem { name, brands?, types? }
 *   - name: druh materiálu ("Sifón umývadlový")
 *   - brands: dostupné značky pre tento druh (["Alcaplast", "HL", "Viega"])
 *   - types: dostupné typy/rozmery (["DN32", "DN40", "DN50"])
 */

export interface MaterialItem {
  name: string
  brands?: string[]
  types?: string[]
}

/** Flat suggestion for name autocomplete dropdown */
export interface MaterialSuggestion {
  name: string
  brand?: string
}

/** Extract 2-digit code from category string like "01. Plumber" */
export function getCategoryCode(category: string): string {
  const match = category.match(/^(\d{2})/)
  return match ? match[1] : ''
}

/**
 * Get flat name suggestions for autocomplete.
 * Returns unique names from category-specific + universal items.
 * Pass lang='cz' for Czech translations.
 */
export function getMaterialSuggestions(category: string, lang?: string): MaterialSuggestion[] {
  const code = getCategoryCode(category)
  const rawItems = [...(MATERIAL_CATALOG[code] || []), ...UNIVERSAL_MATERIALS]
  const items = lang === 'cz' ? rawItems.map(translateItem) : rawItems
  return items.map(i => ({ name: i.name, brand: i.brands?.[0] }))
}

/**
 * Filter suggestions by search query.
 */
export function filterSuggestions(suggestions: MaterialSuggestion[], query: string): MaterialSuggestion[] {
  if (!query || query.length < 1) return suggestions
  const q = stripDiacritics(query)
  return suggestions.filter(
    s => stripDiacritics(s.name).includes(q) || (s.brand && stripDiacritics(s.brand).includes(q))
  )
}

/**
 * Get brand suggestions for a specific material name in a category.
 */
export function getBrandSuggestions(category: string, materialName: string, lang?: string): string[] {
  const code = getCategoryCode(category)
  const rawItems = [...(MATERIAL_CATALOG[code] || []), ...UNIVERSAL_MATERIALS]
  const items = lang === 'cz' ? rawItems.map(translateItem) : rawItems
  const q = stripDiacritics(materialName)
  // Find exact or closest match
  const exact = items.find(i => stripDiacritics(i.name) === q)
  if (exact?.brands) return exact.brands
  // Partial match — merge brands from all matching items
  const brands = new Set<string>()
  items.filter(i => stripDiacritics(i.name).includes(q) || q.includes(stripDiacritics(i.name)))
    .forEach(i => i.brands?.forEach(b => brands.add(b)))
  return Array.from(brands)
}

/**
 * Get type/size suggestions for a specific material name in a category.
 */
export function getTypeSuggestions(category: string, materialName: string, lang?: string): string[] {
  const code = getCategoryCode(category)
  const rawItems = [...(MATERIAL_CATALOG[code] || []), ...UNIVERSAL_MATERIALS]
  const items = lang === 'cz' ? rawItems.map(translateItem) : rawItems
  const q = stripDiacritics(materialName)
  const exact = items.find(i => stripDiacritics(i.name) === q)
  if (exact?.types) return exact.types
  const types = new Set<string>()
  items.filter(i => stripDiacritics(i.name).includes(q) || q.includes(stripDiacritics(i.name)))
    .forEach(i => i.types?.forEach(t => types.add(t)))
  return Array.from(types)
}

