/** Strip diacritics for fuzzy comparison */
export function stripDiacritics(text: string): string {
  return text.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase()
}

/**
 * Find canonical (with-diacritics) name from catalog for a user-typed name.
 * Returns the catalog version if found, otherwise returns input unchanged.
 */
export function findCanonicalName(input: string, category: string, lang?: string): string {
  const code = getCategoryCode(category)
  const rawItems = [...(MATERIAL_CATALOG[code] || []), ...UNIVERSAL_MATERIALS]
  const items = lang === 'cz' ? rawItems.map(translateItem) : rawItems
  const inputStripped = stripDiacritics(input)
  const match = items.find(i => stripDiacritics(i.name) === inputStripped)
  return match ? match.name : input
}

/**
 * Pre-compute Unicode-aware versions of SK_CZ_WORDS patterns.
 * JS \b only recognizes [a-zA-Z0-9_] as word chars, so diacritics like ľ, ý, á
 * break \b word boundaries. We convert \b → (?<!\p{L}) / (?!\p{L}) with /u flag.
 */
const SK_CZ_UNICODE: [RegExp, string][] = SK_CZ_WORDS.map(([pattern, replacement]) => {
  const src = pattern.source
    .replace(/^\\b/, '(?<!\\p{L})')
    .replace(/\\b$/, '(?!\\p{L})')
  return [new RegExp(src, 'gu'), replacement]
})

/** Translate a SK string to CZ using word replacements */
export function skToCz(text: string): string {
  let result = text
  for (const [pattern, replacement] of SK_CZ_UNICODE) {
    result = result.replace(pattern, replacement)
  }
  return result
}

/** Translate a MaterialItem to CZ */
function translateItem(item: MaterialItem): MaterialItem {
  return {
    name: skToCz(item.name),
    brands: item.brands,
    types: item.types?.map(t => skToCz(t)),
  }
}

